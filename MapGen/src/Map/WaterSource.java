package Map;

public class WaterSource {
	
	
	private int x, y;
	
	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public WaterSource(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

	
	

}
